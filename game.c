	DisableInterrupts();
  PLL_Init();
	Output_Init();
	GameInit();
	PortB_Init();
	PortD_Init();
	SysTick_Init(&PBulletActivate,30);
	Timer2_Init(&GameTask,(80000000/30));
	Timer1_Init(&BulletActivate,(80000000/1));
	ADC_Init();
	Sound_Init();
