/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//This function initializes the start of the bullet life cycle at the nose of enemy's airplane, also makes status alive and request to be drawn//
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void BulletActivate(void){
	
	int n, alreadyActivated = 0;
	int m = 0;	//Indicates which enemy, Enemy[0] or Enemy[1]

		//Bullet activate for Enemy[0]
		for(n=0; n<4; n++){
	
			if(Bullet[n].life == dead && alreadyActivated == 0 && Enemy[m].life == alive){
				Bullet[n].x = EnemyPos[0][m] + (Enemy[m].w / 2);
				Bullet[n].y = EnemyPos[1][m];
				Bullet[n].frontHitbox = Bullet[n].y;
				Bullet[n].leftHitbox = Bullet[n].x + 1;
				Bullet[n].rightHitbox = Bullet[n].x + 2;
				Bullet[n].vy = 1;
				Bullet[n].life = alive;
				Bullet[n].lifenum = 1;
				Bullet[n].needDraw = 1;
			
				alreadyActivated = 1;
			}
		}
		
		m++;
		alreadyActivated = 0;
		
		//Bullet activate for Enemy[1]
		for(n=4; n<8; n++){
			
			if(Bullet[n].life == dead && alreadyActivated == 0 && Enemy[m].life == alive){
				Bullet[n].x = EnemyPos[0][m] + (Enemy[m].w / 2);	
				Bullet[n].y = EnemyPos[1][m];
				Bullet[n].frontHitbox = Bullet[n].y;
				Bullet[n].leftHitbox = Bullet[n].x + 1;
				Bullet[n].rightHitbox = Bullet[n].x + 2;
				Bullet[n].vy = 1;
				Bullet[n].life = alive;
				Bullet[n].lifenum = 1;
				Bullet[n].needDraw = 1;
			
				alreadyActivated = 1;
			}
		}
}


//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//This function initializes the start of the bullet life cycle at the nose of player's airplane, also makes status alive and request to be drawn//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void PBulletActivate(void){
	
	int n, alreadyActivated = 0;

		for(n=0; n<3; n++){
	
			if(PBullet[n].life == dead && alreadyActivated == 0){
				PBullet[n].x = PlayerPos[0]	+ (Player[0].w / 2);
				PBullet[n].y = PlayerPos[1] - Player[0].h;
				PBullet[n].frontHitbox = PBullet[n].y + PBullet[n].h;
				PBullet[n].leftHitbox = PBullet[n].x + 1;
				PBullet[n].rightHitbox = PBullet[n].x + 2;
				PBullet[n].vy = -1;
				PBullet[n].life = alive;
				PBullet[n].lifenum = 1;
				PBullet[n].needDraw = 1;
				
				Sound_Shoot();
			
				alreadyActivated = 1;
			}
		}
}

