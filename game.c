unsigned int EnemyPos[2][2];			//Global variable to pass locational coordinate values of enemy entity to BulletActivation function
unsigned int PlayerPos[2];				//Global variable to pass locational coordinate values of player entity to PBulletActivation function
extern signed int playerAdd;			//TODO

void GameMovement(void){					//Updates entities and screen output at 30Hz
	
	unsigned int leftMin,rightMax,frontMax,backMin,start,stop,g,i,j = 0;	//Moved declarations inside function to save memory by not making it global
	victoryOver = 1;																											//Setting the game to be over initially, but if there is enemy, this will be changed to false
	gameOver = 1;
	
	/////////////////////
	//The enemy section//
	/////////////////////
	for(i=0; i<2; i++){
		
		//Boundary control for enemies
		if(Enemy[i].life == alive){
			
			Enemy[i].needDraw = 1;			//GameDraw(); acknowledges the needDraw flag through Enemy[i].needDraw = 0; We set it again here for alive entities so it gets updated by GameDraw(); again
			victoryOver = 0;						//As long as there is an enemy alive, the game does not end as this will keep being set as 0
			
			switch(i){
				
				case 0:
					leftMin = 3;			//leftHitbox < a
					rightMax = 63;		//rightHitbox > b
					frontMax = 97;		//frontHitbox > c
					backMin = 3;			//backHitbox < d
					break;
					
				case 1:
					leftMin = 66;			//leftHitbox < a
					rightMax = 124;		//rightHitbox > b
					frontMax = 97;		//frontHitbox > c
					backMin = 3;			//backHitbox < d
					break;
			}
			
			if(Enemy[i].lifenum == 0){
				Sound_Explosion();
				Enemy[i].life = dead;
			}
			
			//Bounce off boundaries
			if(Enemy[i].leftHitbox < leftMin || Enemy[i].rightHitbox > rightMax){
				Enemy[i].vx *= -1;																											
			}																										 
			
			if(Enemy[i].frontHitbox > frontMax || Enemy[i].backHitbox < backMin){
				Enemy[i].vy *= -1;
			}
				
			Enemy[i].x += Enemy[i].vx;
			Enemy[i].leftHitbox = Enemy[i].x + 2;
			Enemy[i].rightHitbox = Enemy[i].x + Enemy[i].w - 2;
			Enemy[i].y += Enemy[i].vy;
			Enemy[i].frontHitbox = Enemy[i].y - 2;
			Enemy[i].backHitbox = Enemy[i].y - Enemy[i].h;
			
		}
		
		else{}
		
		EnemyPos[0][i] = Enemy[i].x;	//Stores the location of the two enemy airplanes globally
		EnemyPos[1][i] = Enemy[i].y;
			
	}
	
	//////////////////////
	//The player section//
	//////////////////////
	if(Player[0].life == alive){
		
		Player[0].needDraw = 1;
		gameOver = 0;
		
		if(Player[0].lifenum == 0){		//Player is dead after lives drop from 3 to 0
			Player[0].life = dead;
		}
		
		else if(Player[0].leftHitbox < 3){
			playerAdd = 0;
			Player[0].x += 2;
		}
	
		else if(Player[0].rightHitbox > 126){
			playerAdd = 0;
			Player[0].x -= 2;
		}

		Player[0].x += playerAdd;
		Player[0].leftHitbox = Player[0].x;
		Player[0].rightHitbox = Player[0].x + Player[0].w;;
	}
			
	PlayerPos[0] = Player[0].x;	//Stores the location of the two enemy airplanes globally
	PlayerPos[1] = Player[0].y;
	
	//////////////////////
	//The bullet section//
	//////////////////////
	for(i=0; i<8; i++){
		
		if(Bullet[i].life == alive){
			
			Bullet[i].needDraw = 1;
			
				//Boundary control for enemy's bullet
				//Kills bullet if collides with player's boundaries
				if(Bullet[i].frontHitbox > Player[0].frontHitbox && Bullet[i].leftHitbox > Player[0].leftHitbox && Bullet[i].rightHitbox < Player[0].rightHitbox ){
					Bullet[i].life = dead;
					Bullet[i].lifenum -= 1;
					Player[0].lifenum -= 1;
					//deadCount++;	FOR DEBUGGING ONLY!
				}
			
				//Kills bullet if it travels out of the map
				else if(Bullet[i].frontHitbox == 160){
					Bullet[i].life = dead;
					Bullet[i].lifenum -= 1;
				}
				
				else{
					Bullet[i].y += Bullet[i].vy;
					Bullet[i].frontHitbox = Bullet[i].y;
					Bullet[i].backHitbox = Bullet[i].y - Bullet[i].h;
				}
		
		}
		
	}
	
	///////////////////////////////
	//The player's bullet section//
	///////////////////////////////
	for(i=0; i<3; i++){
		
		for(j=0; j<2; j++){	//TODO description of why j is here
		
			if(PBullet[i].life == alive){
			
				PBullet[i].needDraw = 1;
				
				switch(j){
				
				case 0:
					start = 0;
					stop = 4;
					break;
					
				case 1:
					start = 4;
					stop = 8;
					break;
			}
				
				//Kills bullet if collides with enemy's bullet
				for(g=start; g<stop; g++){
					if(PBullet[i].frontHitbox <= Bullet[g].frontHitbox && PBullet[i].leftHitbox >= (Bullet[g].leftHitbox - 1) && PBullet[i].rightHitbox <= Bullet[g].rightHitbox){
						PBullet[i].life = dead;
						PBullet[i].lifenum -= 1;
					}
					
					else{}
				}
			
				//Kills bullet if collides with enemy's boundaries
				if(PBullet[i].frontHitbox <= Enemy[j].frontHitbox && PBullet[i].leftHitbox > Enemy[j].leftHitbox && PBullet[i].rightHitbox < Enemy[j].rightHitbox){
					PBullet[i].life = dead;
					PBullet[i].lifenum -= 1;
					Enemy[j].lifenum -= 1;
					//enemyDeadcount++; 	FOR DEBUGGING ONLY!
				}
					
				//Kills bullet if it travels out of the map
				else if(PBullet[i].frontHitbox == 1){
					PBullet[i].life = dead;
					PBullet[i].lifenum -= 1;
				}
				
				else{
					PBullet[i].y += PBullet[i].vy;
					PBullet[i].frontHitbox = PBullet[i].y - PBullet[i].h;
					PBullet[i].backHitbox = PBullet[i].y;
				}
			}
		}
	}
}


